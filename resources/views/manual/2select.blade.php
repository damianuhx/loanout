<h3>Objektauswahl</h3>

<p>Um Objekte auszuleihen gehen Sie im Hauptmenu auf "Objektauswahl" und wählen im Untermenu die gewünschte Abteilung.
Danach können Sie eine Kategorie auswählen um die darin verfügbaren Modelle aufzulisten. Die farbigen Punkte geben
an ob noch mindestens ein vollständiges Objekt (grün) resp. ein unvollständiges Objekt (orange) verfügbar ist.
Klicken Sie auf das gewünschte Modell, werden alle Objekte des entsprechenden Modells mit Seriennummer aufgelistet.
Objekte mit einem grünen oder orangen Punkt sind ausleihbar. Bei grünen Objekten ist sämtliches benötigtes Zubehör
beiliegend, bei orangen Objekten ist das Zubehör nicht vollständig. Um das Objekt auszuleihen klicken Sie auf das Objekt
und dann auf "ausleihen". Das Objekt befindet sich nun für mindestens 15 Minuten in Ihrem Warenkorb, auf den Sie automatisch weitergeleitet werden.</p>

<p>Wird das Objekt mit einem roten Punkt angezeigt,
so ist es entweder ausgeliehen (das voraussichtliche Rückgabedatum steht in Klammern) oder nicht funktionstüchtig und
kann deshalb nicht ausgeliehen werden.</p>

