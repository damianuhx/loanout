<h3>Ausleihprozedere</h3>

Der Ausleihprozess durchläuft bis zu drei Stufen:
<ul>
    <li>Bewilligen</li>
    <li>Versenden</li>
    <li>Zurücknehmen</li>
</ul>

<p>Unter dem Hauptmenu "Ausleihe" können Sie die Ausleihen der entsprechenden Status anschauen.</p>

<p>Wird das Objekt durch einen normalen User ausgeliehen, muss dies zuerst von einem Supervisor bewilligt werden.
Will ein Supervisor ein Objekt ausleihen so wird es direkt als zum "aushändigen" markiert. Objekte die "auszuhändigen" sind
sollen von einem Supervisor oder Admin versendet oder direkt ausgehändigt werden. Der Lieferschein kann durch den Knopf "PRINT" angezeigt werden.
Durch die Druckoption in Ihrem Browser kann eine Druckversion davon ausgedruckt oder als PDF gespeichert werden.</p>

<p>Wird das Objekt zurückgenommen muss für jedes Objekt die Funktionstüchtigkeit getestet und mit einem entsprechenden Häckchen
bestätigt werden. Zusätzlich soll für jedes Zubehör angegeben werden ob es bei der Rücknahme vorhanden war oder nicht. Zubehör,
das bereits vor der Ausleihe fehlte wird in rot angezeigt. Es ist wünschenswert auch dieses Zubehör wieder zu ergänzen falls möglich.</p>

