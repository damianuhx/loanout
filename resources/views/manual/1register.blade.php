
<h3>Registrieren</h3>

<p>Gehen Sie auf {{env('HREF_MAIN')}}auth/register und geben Sie dort Ihren Benutzername,
Ihre E-Mail Adresse sowie das gewünschte Passwort ein. Nach der Registrierung können Sie
das Tool erst verwenden, wenn ein Supervisor Ihnen die entsprechenden Rechte erteilt hat.
Bitte teilen Sie Ihre Registrierung einem Supervisor direkt mit, damit dieser Ihnen die
Rechte unmittelbar erteilen kann.</p>

