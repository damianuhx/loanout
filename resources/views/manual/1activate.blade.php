<h3>Benutzer aktivieren (nur Supervisor)</h3>

<p>Wählen Sie unter "Einstellungen" und dann unter "Users" den entsprechenden Benutzer aus.
Geben Sie unter "Type" den Nutzertyp an. Dies kann einer dieser drei sein: "supervisor", "admin" oder "user".
Der Supervisor hat alle Rechte, während der User die Ausleihen nur Anfragen kann. Der Admin kann keine
Objekte ausleihen sondern nur das Lager und die Objekte verwalten (d.h. verschicken, zurücknehmen, reinventarisieren).
Sollte eine Person mehrere Aufgaben übernehmen empfielt es sich für diese Person mehrere Konten zu erstellen (z.B. kann
jemand Supervisor und Admin gleichzeitig sein). Unter "Sprache" kann zwischen den verfügbaren Sprachen gewählt werden.</p>

